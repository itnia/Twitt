     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {        
        $request->validate([
            'message' => 'required',
        ]);
        $message = new Message;
        $message->message = $request->message;
        $message->user_id = Auth::id();
        $message->save();
        return redirect()->route('home');
    }

